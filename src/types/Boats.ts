/** Represents the different types of boats in the game. Each value indicates the number of cells the boat occupies. */
export enum Boats {
	/** Aircraft carrier. Occupies 5 cells. */
	AircraftCarrier = 5,
	/** Cruiser. Occupies 4 cells. */
	Cruiser = 4,
	/** Torpedo boat. Occupies 3 cells. */
	TorpedoBoat = 3,
	/** Submarine. Occupies 2 cells. */
	Submarine = 2,
}
